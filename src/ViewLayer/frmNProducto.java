    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (producto.getIdProducto() > 0) {
            producto.setNombre(txtNombre.getText());
            producto.setCaducidad(formato.format(jdCaducidad.getDate()));
            producto.setStock(Integer.parseInt(txtStock.getText()));
            producto.setIdFarmacia(cbFarmacias.getItemAt(cbFarmacias.getSelectedIndex()).getIdFarmacia());
            producto.setActivo(1);
            if (producto.Update()) {
                JOptionPane.showMessageDialog(null, "Se modifico el producto");
                this.dispose();
            }
        } else {
            if (new Producto(0,
                    txtNombre.getText(),
                    formato.format(jdCaducidad.getDate()),
                    Integer.parseInt(txtStock.getText()),
                    cbFarmacias.getItemAt(cbFarmacias.getSelectedIndex()).getIdFarmacia(),
                    1).Add()) {
                JOptionPane.showMessageDialog(null, "El producto fue agregado");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

