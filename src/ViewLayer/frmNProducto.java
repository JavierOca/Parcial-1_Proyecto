    public frmNProducto(int idProducto) {
        initComponents();
        this.setLocationRelativeTo(null);
        farmacia.mostrarFarmacias(cbFarmacias);
        this.idProducto = idProducto;
        producto.setIdProducto(idProducto);
        producto.GetById();
        txtNombre.setText(producto.getNombre());
        try {
            jdCaducidad.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(producto.getCaducidad()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al convertir la fecha");
        }
        txtStock.setText("" + producto.getStock());
        for(int i=0; i<new Farmacia().GetAllModel().getRowCount(); i++) {
            if(new Farmacia().GetAllModel().getValueAt(i, 0).equals(producto.getIdFarmacia())) {
                cbFarmacias.setSelectedIndex(i);
            }
        }
    }

